def get_topic_id(db, topic_name: str, exam_id: int):
    """Find topic by name (case-insensitive partial match)."""
    topic = db.query(Topic).filter(
        Topic.name.ilike(f"%{topic_name}%"),
        Topic.exam_id == exam_id
    ).first()
    return topic.id if topic else None


