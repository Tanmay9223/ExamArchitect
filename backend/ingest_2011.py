def crop_and_save_diagram(pdf_path: Path, page_index: int, bbox_normalized: list,
                           out_path: Path) -> bool:
    """
    Crop a region from a PDF page and save as PNG.
    bbox_normalized = [ymin, xmin, ymax, xmax] all in 0.0-1.0 range.
    page_index is 0-based.
    """
    try:
        doc = fitz.open(str(pdf_path))
        page = doc[page_index]
        pw, ph = page.rect.width, page.rect.height

        ymin, xmin, ymax, xmax = bbox_normalized
        # Clamp to valid range
        xmin = max(0.0, min(1.0, xmin))
        xmax = max(0.0, min(1.0, xmax))
        ymin = max(0.0, min(1.0, ymin))
        ymax = max(0.0, min(1.0, ymax))

        rect = fitz.Rect(xmin * pw, ymin * ph, xmax * pw, ymax * ph)
        if rect.width < 10 or rect.height < 10:
            print(f"  [WARN] Diagram bbox too small, skipping crop")
            doc.close()
            return False

        pix = page.get_pixmap(clip=rect, dpi=150)
        out_path.parent.mkdir(parents=True, exist_ok=True)
        pix.save(str(out_path))
        doc.close()
        return True
    except Exception as e:
        print(f"  [ERROR] Diagram crop failed: {e}")
        return False


