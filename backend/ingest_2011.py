def main():
    # ── Gemini setup ─────────────────────────────────────────────────
    api_key = os.getenv("GEMINI_API_KEY")
    if not api_key:
        print("ERROR: GEMINI_API_KEY not set. Add it to backend/.env")
        sys.exit(1)

    genai.configure(api_key=api_key)
    client = genai.GenerativeModel(GEMINI_MODEL)
    print(f"Gemini model: {GEMINI_MODEL}")

    # ── DB: find GATE-CS 2011 paper ─────────────────────────────────────────
    db = SessionLocal()
    try:
        exam = db.query(Exam).filter(Exam.name == "GATE-CS").first()
        if not exam:
            print("ERROR: GATE-CS exam not found in database. Run init_db first.")
            sys.exit(1)

        paper = db.query(Paper).filter(
            Paper.exam_id == exam.id,
            Paper.year == 2011
        ).first()
        if not paper:
            print("ERROR: GATE CS 2011 paper not found. Run init_db first.")
            sys.exit(1)

        paper_id = paper.id
        print(f"Found paper: GATE CS 2011 (ID={paper_id})")

        # ── Delete ONLY synthetic placeholder questions (preserve real extracted ones) ──
        # Synthetic placeholders contain the marker text "Could not be extracted"
        # Real questions extracted by Gemini don't contain this text.
        all_questions = db.query(Question).filter(Question.paper_id == paper_id).all()
        synthetic_ids = [
            q.id for q in all_questions
            if "Could not be extracted" in q.question_text
        ]
        if synthetic_ids:
            db.query(Question).filter(Question.id.in_(synthetic_ids)).delete(synchronize_session=False)
            db.commit()
            print(f"Deleted {len(synthetic_ids)} synthetic placeholder questions (preserving {len(all_questions) - len(synthetic_ids)} real questions)")
        else:
            print("No synthetic placeholders found — starting fresh")
            deleted = db.query(Question).filter(Question.paper_id == paper_id).delete()
            db.commit()
            print(f"Deleted {deleted} questions")

        # Also collect already-extracted q_nums so we don't re-extract them
        existing_real = db.query(Question).filter(Question.paper_id == paper_id).all()
        already_extracted_nums = {
            q.question_number for q in existing_real
            if "Could not be extracted" not in q.question_text
        }
        if already_extracted_nums:
            print(f"Already have real extractions for Q: {sorted(already_extracted_nums)}")

    except Exception as e:
        db.close()
        print(f"DB setup error: {e}")
        raise

    # ── Extract questions from pages 2–17 (page 1 = instructions only) ──────
    # questions_map: {q_num -> extracted dict} — later pages overwrite earlier
    # ones for the same question number (handles page-spanning questions)
    questions_map: dict = {}

    total_pages = 9  # Only process page 9 which contains Q32-Q35
    for page_num in range(START_PAGE, total_pages + 1):
        image_path = PAGES_DIR / f"page_{page_num}.png"
        print(f"\n[Page {page_num}/{total_pages}] {image_path.name} ...", flush=True)

        extracted = extract_questions_from_page(client, image_path)
        print(f"  → Extracted {len(extracted)} question(s) from this page")

        for q in extracted:
            q_num = q.get("question_number")
            if not q_num or not isinstance(q_num, int):
                continue
            if q_num < 1 or q_num > 65:
                print(f"  [SKIP] q_num={q_num} out of range 1-65")
                continue
            if q_num in already_extracted_nums:
                print(f"  [SKIP] Q{q_num} already in DB (real extraction), skipping")
                continue
            questions_map[q_num] = q  # later page overwrites (deduplication)

        if page_num < total_pages:
            print(f"  Sleeping {SLEEP_BETWEEN_CALLS}s to respect rate limit...")
            time.sleep(SLEEP_BETWEEN_CALLS)

    print(f"\n{'='*60}")
    print(f"Total unique questions extracted: {len(questions_map)}")
    print(f"{'='*60}")

    # ── Insert questions into DB ─────────────────────────────────────────────
    inserted_count = 0
    skipped_count = 0
    diagram_count = 0

    slices_dir_for_paper = SLICES_DIR / str(paper_id)
    slices_dir_for_paper.mkdir(parents=True, exist_ok=True)

    for q_num in sorted(questions_map.keys()):
        q_data = questions_map[q_num]
        q_text = q_data.get("question_text", "").strip()

        if not q_text:
            print(f"  [SKIP] Q{q_num}: empty question text")
            skipped_count += 1
            continue

        # Classification
        classification = classify_question(q_text, q_num)
        marks = get_marks(q_num)
        correct_answer = CODE_A_ANSWERS.get(q_num)

        # Difficulty based on word count
        word_count = len(q_text.split())
        difficulty = "H" if word_count > 200 else ("M" if word_count > 80 else "E")

        # Topic lookup
        topic_id = None
        chapter = classification["suggested_chapter"]
        subject = classification["suggested_subject"]
        if chapter:
            topic_id = get_topic_id(db, chapter, exam.id)
        if not topic_id and subject:
            topic_id = get_topic_id(db, subject, exam.id)

        # Diagram handling
        has_diagram = bool(q_data.get("has_diagram", False))
        diagram_path_db = None
        bbox = q_data.get("diagram_bbox")

        if has_diagram and bbox and isinstance(bbox, list) and len(bbox) == 4:
            # PDF page index is 0-based; page_num from the loop corresponds to
            # the page where Gemini last saw this question. We need to find
            # which page_num belongs to q_num. Since we've already closed the
            # page loop, we do a best-effort: map q_num to approximate page.
            # Questions are spread pages 2–15 for Q1-Q55, pages 16-17 for GA.
            # Use a rough heuristic: 4 questions per page on average.
            approx_page = 2 + max(0, (q_num - 1) // 4)
            approx_page = min(approx_page, 15)
            if q_num >= 56:
                approx_page = 16

            slice_filename = f"q_{q_num}.png"
            slice_out_path = slices_dir_for_paper / slice_filename
            success = crop_and_save_diagram(
                PDF_PATH,
                page_index=approx_page - 1,  # 0-based
                bbox_normalized=bbox,
                out_path=slice_out_path,
            )
            if success:
                diagram_path_db = f"/slices/{paper_id}/{slice_filename}"
                diagram_count += 1
                print(f"  [DIAGRAM] Q{q_num} → {slice_out_path.name}")

        question_obj = Question(
            paper_id=paper_id,
            topic_id=topic_id,
            question_number=q_num,
            question_text=q_text[:2000],   # DB text field limit guard
            question_style=q_data.get("question_style", "MCQ"),
            difficulty=difficulty,
            marks=marks,
            correct_answer=correct_answer,
            has_diagram=has_diagram,
            diagram_path=diagram_path_db,
        )
        db.add(question_obj)
        inserted_count += 1

    db.commit()
    print(f"\nInserted {inserted_count} questions  |  Skipped {skipped_count}  |  Diagrams saved {diagram_count}")

    # ── Fill missing questions with synthetic placeholders if needed ─────────
    existing_nums = set(questions_map.keys()) | already_extracted_nums
    missing_nums = [n for n in range(1, 66) if n not in existing_nums]
    if missing_nums:
        print(f"\n[WARN] {len(missing_nums)} questions were NOT extracted: {missing_nums}")
        print("Inserting minimal synthetic placeholders for missing questions...")
        for q_num in missing_nums:
            classification = classify_question("", q_num)
            marks = get_marks(q_num)
            topic_id = get_topic_id(db, classification["suggested_chapter"], exam.id) or \
                       get_topic_id(db, classification["suggested_subject"], exam.id)
            q_obj = Question(
                paper_id=paper_id,
                topic_id=topic_id,
                question_number=q_num,
                question_text=f"[Q.{q_num} — Could not be extracted from scanned PDF. Refer to the original question paper.]",
                question_style="MCQ",
                difficulty="M",
                marks=marks,
                correct_answer=CODE_A_ANSWERS.get(q_num),
                has_diagram=False,
                diagram_path=None,
            )
            db.add(q_obj)
        db.commit()
        print(f"Added {len(missing_nums)} synthetic placeholders.")

    # ── Update paper metadata ────────────────────────────────────────────────
    paper.total_questions = 65
    paper.is_processed = True
    db.commit()
    print(f"\nUpdated paper: total_questions=65, is_processed=True")

    # ── Recompute TopicYearStats for 2011 ────────────────────────────────────
    recompute_topic_stats(db, exam_id=exam.id, year=2011)
