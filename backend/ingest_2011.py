def get_marks(q_num: int) -> float:
    """Return the marks for question q_num per the 2011 paper structure."""
    if 1 <= q_num <= 25:
        return 1.0
    elif 26 <= q_num <= 55:
        return 2.0
    elif 56 <= q_num <= 60:
        return 1.0   # GA 1-mark
    elif 61 <= q_num <= 65:
        return 2.0   # GA 2-mark
    return 1.0       # fallback

