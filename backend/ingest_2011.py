EXTRACTION_PROMPT = """You are extracting exam questions from a scanned image of a GATE CS 2011 question paper page.

IMPORTANT RULES:
1. Extract EVERY question visible on this page.
2. For the question text: slightly paraphrase the prose/wording (swap synonyms, reorder non-critical words) to avoid verbatim reproduction — but preserve ALL technical content exactly: variable names, values, formulas, code snippets, option labels (A/B/C/D), and numerical data.
3. Include all four options (A), (B), (C), (D) in the question_text field, formatted as:
   "(A) <option text>\\n(B) <option text>\\n(C) <option text>\\n(D) <option text>"
4. If a question has a diagram, table, or figure that is essential to answering it, set has_diagram to true and provide diagram_bbox as [ymin, xmin, ymax, xmax] in normalized 0.0-1.0 coordinates relative to the full page image.
5. question_style must be one of: "MCQ", "NAT", "MSQ"
6. Return ONLY a valid JSON array. Double-escape all backslashes (e.g. use \\\\log instead of \\log, and \\\\sigma instead of \\sigma) so it is a valid JSON string. No markdown, no explanation, no code fences.

Output format (JSON array of objects):
[
  {
    "question_number": <integer>,
    "question_text": "<full question stem + all 4 options as described>",
    "question_style": "MCQ",
    "has_diagram": false,
    "diagram_bbox": null
  }
]

If this page contains only instructions, headers, or blank/rough-work space with no questions, return: []
"""


def repair_json_backslashes(raw_json: str) -> str:
    # Match a backslash not preceded by another backslash, and not followed by valid JSON escape character.
    pattern = r'(?<!\\)\\(?!["\\/bfnrtu])'
    return re.sub(pattern, r'\\\\', raw_json)


def extract_questions_from_page(model, page_image_path: Path) -> list:
    """Send one page image to Gemini and return list of extracted question dicts."""
    if not page_image_path.exists():
        print(f"  [WARN] Image not found: {page_image_path}")
        return []

    try:
        img = Image.open(page_image_path)
        response = model.generate_content([EXTRACTION_PROMPT, img])

        # Check for finish reason indicating copyright block
        if hasattr(response, "candidates") and response.candidates:
            candidate = response.candidates[0]
            finish_reason = getattr(candidate, "finish_reason", None)
            finish_reason_str = str(finish_reason).upper()
            if "RECITATION" in finish_reason_str or finish_reason == 4 or finish_reason_str == "4":
                print(f"  [WARN] Copyright recitation block on {page_image_path.name}")
                return []

        raw_text = response.text.strip()

        # Strip markdown code fences if the model wraps in ```json ... ```
        if raw_text.startswith("```"):
            raw_text = re.sub(r"^```(?:json)?\s*", "", raw_text)
            raw_text = re.sub(r"\s*```$", "", raw_text)
            raw_text = raw_text.strip()

        if not raw_text or raw_text == "[]":
            return []

        # Repair unescaped LaTeX backslashes before loading
        raw_text = repair_json_backslashes(raw_text)

        data = json.loads(raw_text)
        if not isinstance(data, list):
            print(f"  [WARN] Unexpected response format on {page_image_path.name}")
            return []

        return data

    except json.JSONDecodeError as e:
        print(f"  [ERROR] JSON parse failed on {page_image_path.name}: {e}")
        return []
    except Exception as e:
        print(f"  [ERROR] Gemini call failed on {page_image_path.name}: {e}")
        return []



